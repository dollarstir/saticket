function login($email,$password,$login) 
{

    include "db.php";
    if($login == "") {
        echo    "please select account type";
    } else {
        $log=mysqli_query($conn,"SELECT * FROM $login WHERE contact ='$email' AND password='$password'");
        $row=mysqli_fetch_array($log);
        if ($row > 0) {
            // session_start();
         $_SESSION['id']=$row['id'];
         $_SESSION['name']=$row['name'];
         $_SESSION['email']=$row['contact'];
            if ($login =='admin') {
                
                echo '<script>window.location= "home.php"</script>';
            }
            elseif ($login == 'subadmin') {
                $_SESSION['loc']=$row['location'];
                $_SESSION['team']=$row['team'];
                echo '<script>window.location= "../subadmin/home.php"</script>';
            }
            else {
                $_SESSION['loc']=$row['location'];
                echo '<script>window.location= "../volunt/home.php"</script>';
            
            } 
        } else {
            echo    "please enter correct details";
        }
    }



}






